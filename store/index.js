export const getters = {
    readUser(state){
        return !!state.user
    },
    readPosts(state){
        return state.posts
    },
    readPost(state){
        return state.post
    },
    readPerfil(state){
        return state.perfil
    },
    readAuthor(state){
        return state.author
    }
}

export const mutations = {
    setUser(state, payload){
        state.user = payload
    },
    setPosts(state, payload){
        state.posts.push(payload)
    },
    deletePost(state, payload) {
        state.posts = state.posts.filter( item => item.id != payload)
    },
    selectPost(state, payload){
        state.post = state.posts.find(item => item.id == payload)
    },
    editPost(state, payload){
        const x = state.posts.map(item => item.id == payload.id ? payload : item)
        state.posts = x
    },
}

export const actions = {
   savePost({ commit }, post) {
       commit('setPosts', post)
   },
   deletePost({commit}, id){
       commit('deletePost', id)
   }
}